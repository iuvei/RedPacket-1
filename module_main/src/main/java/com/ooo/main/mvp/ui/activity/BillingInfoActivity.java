public class BillingInfoActivity extends BaseActivity <BillingInfoPresenter> implements BillingInfoContract.View {

    @BindView(R2.id.iv_back)
    ImageView ivBack;
    @BindView(R2.id.tv_title)
    TextView tvTitle;
    @BindView(R2.id.tv_get_money)
    TextView tvGetMoney;
    @BindView(R2.id.tv_type)
    TextView tvType;
    @BindView(R2.id.tv_take_money_time)
    TextView tvTakeMoneyTime;
    @BindView(R2.id.tv_remark)
    TextView tvRemark;
    private WithdrawalRecordBean recordBean;

    @Override
    public void setupActivityComponent(@NonNull AppComponent appComponent) {
        DaggerBillingInfoComponent //如找不到该类,请编译一下项目
                .builder ()
                .appComponent ( appComponent )
                .view ( this )
                .build ()
                .inject ( this );
    }

    @Override
    public int initView(@Nullable Bundle savedInstanceState) {
        return R.layout.activity_billing_info; //如果你不需要框架帮你设置 setContentView(id) 需要自行设置,请返回 0
    }

    @Override
    public void initData(@Nullable Bundle savedInstanceState) {
        StatusBarUtils.setTranslucentStatus ( this );
        StatusBarUtils.setStatusBarDarkTheme ( this, true );
        tvTitle.setText ( "账单详情" );
        recordBean = getIntent ().getParcelableExtra ( "info" );
        if (recordBean != null) {
            tvGetMoney.setText ( recordBean.getAccountMoney () );
            tvRemark.setText ( "备注" );
            tvType.setText ( "支出" );
            tvTakeMoneyTime.setText ( recordBean.getTakeMoneyTime () );
        }
    }

    @Override
    public void showLoading() {

    }

    @Override
    public void hideLoading() {

    }

    @Override
    public void showMessage(@NonNull String message) {
        checkNotNull ( message );
        ArmsUtils.snackbarText ( message );
    }

    @Override
    public void launchActivity(@NonNull Intent intent) {
        checkNotNull ( intent );
        ArmsUtils.startActivity ( intent );
    }

    @Override
    public void killMyself() {
        finish ();
    }

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate ( savedInstanceState );
        // TODO: add setContentView(...) invocation
        ButterKnife.bind ( this );
    }

    @OnClick(R2.id.iv_back)
    public void onViewClicked() {
    }
}
